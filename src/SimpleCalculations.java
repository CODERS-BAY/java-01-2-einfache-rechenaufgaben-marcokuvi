public class SimpleCalculations {
    public static void main (String [] args) {
        int a = 27;
        int b = 45;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        double c = 23.5;
        double d = 36.7;

        System.out.println(c + d);
        System.out.println(Math.round((c - d) * 100) /100);
        System.out.println(c * d);
        System.out.println(Math.round((c / d) * 100) /100);


    }
}
